package startup;

/**
 * This enum contains keys specifying the one-line text prompts that can be printed
 * to console during interactions in <code>AccountCreationController</code>.
 */
enum AccountCreationPrompts {

    USERNAME_PROMPT,
    USERNAME_IN_USE,
    USERNAME_HAS_SPACE_CHAR,
    PASSWORD_PROMPT,
    USERTYPE_PROMPT,
    INVALID_USERTYPE,
    PASSWORD_NO_CAPITAL,
    PASSWORD_NO_LOWER,
    PASSWORD_NO_NUMBER,
    PASSWORD_NO_SPECIAL,
    PASSWORD_SUCCESS,
    PASSWORD_INVALID,
    USERNAME_SUCCESS,
    PASSWORD_NO_LENGTH

}
