package startup;

/**
 * This enum contains keys specifying the one-line text prompts that can be printed
 * to console during interactions in <code>StartUpController</code>.
 */
enum StartUpPrompts {

    WELCOME_MESSAGE,
    USERNAME_PROMPT,
    PASSWORD_PROMPT,
    INPUT_ERROR,
    NO_SUCH_USERNAME_ERROR,
    WRONG_PASSWORD_ERROR,
    ORGANIZER_CREATION

}
