package view;

/**
 * The <code>ViewPrompts</code> enum contains keys specifying the one-line text prompts that can be printed
 * to the console during interactions in <code>ViewController</code> which occur when the Organizer is viewing
 * conference information.
 */
enum ViewPrompts {

    USER_TYPE_PROMPT,
    INVALID_INPUT_ERROR,
    NO_USERS_OF_TYPE_ERROR,
    EVENT_TYPE_PROMPT,
    NO_EVENTS_OF_TYPE_ERROR,
    NO_EVENTS_ERROR,
    NO_USERS_ATTENDING_EVENTS_ERROR,
    NO_SPEAKERS_ERROR
}
