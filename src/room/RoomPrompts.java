package room;

/**
 * This enum contains keys specifying the one-line text prompts
 * that can be printed to console during interactions in <code>RoomController</code>.
 */
enum RoomPrompts {

    ROOM_ID_PROMPT,
    ROOM_CAPACITY_PROMPT,
    INVALID_NUM_ERROR,
    ROOM_EXISTS_ERROR,
    NO_ROOMS_ERROR,
    ROOM_ADDED_SUCCESS,
    ROOM_DELETE_PROMPT,
    ROOM_DELETE_SUCCESS,
    ROOM_DELETE_FAIL,
    ROOM_HAS_EVENTS,
    DELETE_ROOM_PROMPT

}
